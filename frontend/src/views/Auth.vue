<script setup>
import { ref, computed, onUnmounted } from 'vue'
import { useRouter } from 'vue-router'

const router = useRouter()

// Mobile menu state
const isMobileMenuOpen = ref(false)

// Auth state
const isLogin = ref(true)
const submitting = ref(false)
const codeSending = ref(false)
const countdown = ref(0)
let countdownTimer = null

// Form data
const formData = ref({
  phone: '',
  code: ''
})

// Form errors
const errors = ref({})

// Computed properties
const canSendCode = computed(() => {
  return formData.value.phone.length === 11 && /^1[3-9]\d{9}$/.test(formData.value.phone) && countdown.value === 0
})

// Methods
const toggleMobileMenu = () => {
  isMobileMenuOpen.value = !isMobileMenuOpen.value
}

const toggleAuthMode = () => {
  isLogin.value = !isLogin.value
  clearErrors()
  resetForm()
}

const resetForm = () => {
  formData.value = {
    phone: '',
    code: ''
  }
}

const clearErrors = () => {
  errors.value = {}
}

const validateForm = () => {
  clearErrors()
  
  // Validate phone number
  if (!formData.value.phone) {
    errors.value.phone = 'è¯·è¾“å…¥æ‰‹æœºå·ç '
    return false
  }
  
  if (!/^1[3-9]\d{9}$/.test(formData.value.phone)) {
    errors.value.phone = 'è¯·è¾“å…¥æ­£ç¡®çš„æ‰‹æœºå·ç '
    return false
  }
  
  // Validate verification code
  if (!formData.value.code) {
    errors.value.code = 'è¯·è¾“å…¥éªŒè¯ç '
    return false
  }
  
  if (formData.value.code.length !== 6) {
    errors.value.code = 'è¯·è¾“å…¥6ä½éªŒè¯ç '
    return false
  }
  
  return true
}

const sendVerificationCode = async () => {
  if (!canSendCode.value) return
  
  try {
    codeSending.value = true
    
    // æ¨¡æ‹Ÿå‘é€éªŒè¯ç APIè°ƒç”¨
    await new Promise(resolve => setTimeout(resolve, 1000))
    
    // å¼€å§‹å€’è®¡æ—¶
    countdown.value = 60
    countdownTimer = setInterval(() => {
      countdown.value--
      if (countdown.value <= 0) {
        clearInterval(countdownTimer)
        countdownTimer = null
      }
    }, 1000)
    
    console.log('éªŒè¯ç å·²å‘é€åˆ°:', formData.value.phone)
    
  } catch (error) {
    console.error('å‘é€éªŒè¯ç å¤±è´¥:', error)
  } finally {
    codeSending.value = false
  }
}

const handleSubmit = async () => {
  if (!validateForm()) return
  
  try {
    submitting.value = true
    
    // æ¨¡æ‹Ÿç™»å½•/æ³¨å†ŒAPIè°ƒç”¨
    await new Promise(resolve => setTimeout(resolve, 1500))
    
    console.log(isLogin.value ? 'ç™»å½•æˆåŠŸ' : 'æ³¨å†ŒæˆåŠŸ', formData.value)
    
    // ç™»å½•/æ³¨å†ŒæˆåŠŸåè·³è½¬åˆ°é¦–é¡µ
    router.push('/')
    
  } catch (error) {
    console.error(isLogin.value ? 'ç™»å½•å¤±è´¥:' : 'æ³¨å†Œå¤±è´¥:', error)
  } finally {
    submitting.value = false
  }
}

// Cleanup
onUnmounted(() => {
  if (countdownTimer) {
    clearInterval(countdownTimer)
  }
})
</script>

